#!/usr/bin/env python3
import requests
from app.gemini.telegran_key import TELEGRAM_TOKEN  # Importas tu token como lo tienes actualmente

# Configuraci√≥n
WEBHOOK_URL = "https://tudominio.com/webhook"  # Reemplaza con tu dominio real
DROP_PENDING_UPDATES = True  # Borra mensajes pendientes no procesados

def set_webhook():
    try:
        print("üîÑ Configurando webhook...")
        
        response = requests.post(
            f"https://api.telegram.org/bot{TELEGRAM_TOKEN}/setWebhook",
            json={
                'url': WEBHOOK_URL,
                'drop_pending_updates': DROP_PENDING_UPDATES
            },
            timeout=10
        )
        
        result = response.json()
        if result.get('ok'):
            print("‚úÖ Webhook configurado correctamente")
            print(f"üîó URL: {WEBHOOK_URL}")
            print(f"‚ÑπÔ∏è Info: {result}")
        else:
            print("‚ùå Error al configurar webhook:")
            print(result)
            
    except Exception as e:
        print(f"üî• Error inesperado: {str(e)}")

def check_webhook():
    try:
        print("\nüîç Verificando configuraci√≥n actual...")
        response = requests.get(
            f"https://api.telegram.org/bot{TELEGRAM_TOKEN}/getWebhookInfo",
            timeout=5
        )
        print(response.json())
    except Exception as e:
        print(f"‚ö†Ô∏è Error al verificar webhook: {str(e)}")

if __name__ == "__main__":
    set_webhook()
    check_webhook()